extern int	g_tab[4][4];
extern int	g_possible_tab[24][4];

void	ft_change_line(int	y, int	i)
{
	int		x;

	x = 0;
	while (x < 4)
	{
		g_tab[y][x] = g_possible_tab[i][x];
		x++;
	}
}
