#include <ft.h>

int	ft_add(int a, int b)
{
	a = a + b;
	ft_putnbr(a);
	return (0);
}

int	ft_sub(int a, int b)
{
	a = a - b;
	ft_putnbr(a);
	return (0);
}

int	ft_div(int a, int b)
{
	a = a / b;
	ft_putnbr(a);
	return (0);
}

int	ft_mult(int a, int b)
{
	a = a * b;
	ft_putnbr(a);
	return (0);
}

int	ft_mod(int a, int b)
{
	a = a % b;
	ft_putnbr(a);
	return (0);
}
